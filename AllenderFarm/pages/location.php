<h2>
	Directions:
</h2>
<p>
	There are two main ways to get here the first is very scenic and avoids Barnstaple which can be very congested and busy. The second route allows those wanting to shop at Tesco, Sainsburys or other major shops the opportunity to do so and goes through Barnstaple.
</p>
<p>
	BBC Radio Devon on FM 94.80 and Lantern FM 96.20 give up to date travel and weather reports and are well worth listening to.
</p>
<p>
	The ordinance survey grid reference for Allender Farm on Landranger 180, Barnstable & Ilfracombe is SS 489 454 and is identified as Sharftsboro Fm. (our neighbors)
</p>
<p>
	The North Devon link road (A361) from Barnstaple joins the M5 at junction 27 near Tiverton. There are railway stations at Barnstaple and Tiverton as well as regional airports at Exeter and Bristol.
</p>

<p>
	Here are  two different routes to our farm.
</p>

<div id="accordion">
	<h3>
		Route 1
	</h3>
	
	<div>
		<p>
			From the M5 junction 27 take the A361 towards Barnstaple until the roundabout with the A399. At this roundabout turn right on the A399 towards Blackmoor Gate - this will also be marked for Woolacombe and Ilfracombe. Proceed through Blackmoor Gate and on for a short distance before turning left on the A3123 towards Woolacombe and Ilfracombe. Proceed straight on to the Mullacott Cross roundabout. At this roundabout go straight on towards Woolacombe on the B3343 for 1 1/2 miles and turn right at a sign marked Lincombe &amp; Lee. Continue along this road until you pass over a small hump back bridge crossing the former railway line and directly after turn left into a lane which has the Allender Farm and Shaftsboro Farm name plates on the embankment. Continue along this lane for about 1/2 mile to the end where the property is easily identifiable.
		</p>
	</div>

	<h3>
		Route 2
	</h3>
	
	<div>
		<p>
			From the M5 junction 27 take the A361 towards Barnstaple. From Barnstaple, turn right on the A39 at the roundabout marked: hospital A&amp;E and Combe Martin. Continue along through one set of traffic lights, past the hospital (on the right) and through the traffic lights at the hospital entrance to a left fork turning marked Ilfracombe B3230. Continue along this road for approximately 8 miles to a cross roads on the top of a hill. At this crossroads take the left turn on the A3123 marked Mortehoe and Woolacombe. Continue on approximately 2 miles to the Mullacott Cross roundabout. Proceed straight across on to the B3343 for a further 1 1/2 miles to a turning on the right marked Lee, Lincombe and Lee. Continue along this road until you pass over a small hump back bridge crossing the former railway line and directly after turn left into a lane which has the Allender Farm and Shaftsboro Farm name plates on the embankment. Continue along this lane for about 1/2 mile to the end where the property is easily identifiable. 
		</p>
	</div>
</div>
