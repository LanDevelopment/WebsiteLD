<h3>
	The Farm
</h3>

<p>
	Allender Farm is a traditional North Devon farmhouse built at the turn of the last century. It is situated at the end of a long attractive lane of half a mile and offers outstanding views of the Lee Valley and surrounding countryside. Set in delightful grounds of just over 3 acres, the property offers the seclusion of the countryside, for those wanting to get away from it all - yet is only 3 miles from the 'Blue Flag' beach at Woolacombe with its outstanding sandy expanse and Saunton Sands. The beach extends to some 3 miles and connects to Putsborough. We are also within easy reach of the beaches at Mortehoe, Croyde.
</p>

