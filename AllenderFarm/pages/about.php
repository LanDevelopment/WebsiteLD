<h2>
	<img src="img/slideshow/primose.JPG" style="float:left;margin:0 20px 1em 0;width:370px;">
	The Farm
</h2>

<p>
	Allender Farm is a traditional North Devon farmhouse built at the turn of the last century. It is situated at the end of a long attractive drive of half a mile and offers outstanding views of the Lee Valley and surrounding countryside. Set in delightful grounds of just over 3 acres, the property offers the seclusion of the country, for those wanting to get away from it all - yet is only 3 miles from the 'Blue Flag' beach at Woolacombe with its outstanding sandy expanse. The beach extends to some 3 miles and connects to Putsborough. We are also within easy reach of the beaches at Mortehoe, Croyde.
</p>

<p>
	We have B-B-Q's  for those wishing to cook outside and enjoy the un-spoilt views when eating.
</p>

<p>
	A twenty five minute walk down an adjacent footpath through fields and woods leads to the village of Lee. At low tide a small sandy beach is exposed with rock pools and is a delight for the children and grown-ups too.
</p>
